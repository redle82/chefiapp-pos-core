async function checkTables() {
    const { data, error } = await supabase
        .from('information_schema.tables')
        .select('table_name')
        .eq('table_schema', 'public');

    if (error) console.error(error);
    else console.log('Tables:', data.map(t => t.table_name).sort());

    await supabase.rpc('reload_schema'); // Try to reload if supported, usually requires custom function
}

